#!/usr/bin/env ruby -w
# encoding: UTF-8
class C字符串匹配器
  def self.目录文件匹配(目录路径, 文件后缀 = '.*')
    S目录.遍历文件路径(目录路径) do |路径|
      if 文件后缀 == '.*' || 文件后缀.含有?(R文件.扩展名(路径))
        字符串数组 = 打开(路径,'r:UTF-8'){|文件| 文件.逐行读取}
        字符串数组.每个索引 do |索引|
          yield("#{路径}:#{索引 + 1}:", 字符串数组[索引])
        end
      end
    end
  end
end
